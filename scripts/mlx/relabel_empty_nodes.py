def main():
    src = Path("data/finetune/tasks/extract-core/train.jsonl")
    if not src.exists():
        raise SystemExit(f"파일 없음: {src}")

    lines = src.read_text().splitlines()
    out_lines = []
    changed = 0
    kept_empty = 0

    for i, line in enumerate(lines):
        d = json.loads(line)
        if i in RELABEL:
            # assistant 메시지의 nodes를 새 라벨로 교체
            for m in d["messages"]:
                if m["role"] == "assistant":
                    ast = json.loads(m["content"])
                    ast["nodes"] = RELABEL[i]
                    m["content"] = json.dumps(ast, ensure_ascii=False)
            changed += 1
        elif i in KEEP_EMPTY:
            kept_empty += 1
        out_lines.append(json.dumps(d, ensure_ascii=False))

    # 백업
    backup = src.with_suffix(src.suffix + ".pre-relabel-backup")
    if not backup.exists():
        backup.write_bytes(src.read_bytes())
        print(f"백업: {backup.name}")

    src.write_text("\n".join(out_lines) + "\n")
    print(f"재라벨 적용: {changed}건")
    print(f"빈 배열 유지: {kept_empty}건")

    total_empty_before = len(RELABEL) + len(KEEP_EMPTY)
    print(f"대상 총합: {total_empty_before}건 (143건 중)")
    uncovered = 143 - total_empty_before
    if uncovered:
        print(f"⚠️ 미처리: {uncovered}건 — 목록 점검 필요")

    # 검증: 빈 배열 샘플 수 확인
    new_empty = 0
    for line in out_lines:
        d = json.loads(line)
        ast_c = next(m["content"] for m in d["messages"] if m["role"] == "assistant")
        if not json.loads(ast_c).get("nodes"):
            new_empty += 1
    print(f"검증: 재적용 후 빈 배열 샘플 {new_empty}건 (기존 143 → {new_empty})")


if __name__ == "__main__":
    main()
