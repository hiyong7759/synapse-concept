def make_record(question: str, keywords: list[str]) -> dict:
    return {
        "messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": f"질문: {question}"},
            {"role": "assistant", "content": json.dumps(keywords, ensure_ascii=False)},
        ]
    }


def cmd_build(_args) -> None:
    AUG_DIR.mkdir(parents=True, exist_ok=True)
    # 중복 체크: 기존 질문 set
    existing = set()
    with (TASK_DIR / "train.jsonl").open() as f:
        for line in f:
            d = json.loads(line)
            u = next(m["content"] for m in d["messages"] if m["role"] == "user")
            existing.add(u.replace("질문:", "").strip())
    dups = [q for q, _, _ in SAMPLES if q in existing]
    if dups:
        print(f"[경고] 기존과 중복되는 질문 {len(dups)}건:", file=sys.stderr)
        for q in dups:
            print(f"  - {q}", file=sys.stderr)
        sys.exit(1)
    # 길이 체크
    bad = [(q, len(kw)) for q, kw, _ in SAMPLES if not (5 <= len(kw) <= 8)]
    if bad:
        print(f"[경고] 키워드 길이 범위 밖: {bad}", file=sys.stderr)
        sys.exit(1)
    # 유형별 개수 체크
    type_counts = Counter(t for _, _, t in SAMPLES)
    records = [make_record(q, kw) for q, kw, _ in SAMPLES]
    with AUG_FILE.open("w") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"작성 완료: {len(records)}건 → {AUG_FILE}")
    print("유형별 분포:")
    for t, c in sorted(type_counts.items()):
        print(f"  {t:12s} {c}")


def cmd_stats(_args) -> None:
    type_counts = Counter(t for _, _, t in SAMPLES)
    lens = [len(kw) for _, kw, _ in SAMPLES]
    print(f"총 {len(SAMPLES)}건")
    for t, c in sorted(type_counts.items()):
        print(f"  {t:12s} {c}")
    print(f"키워드 길이: min={min(lens)} max={max(lens)} mean={sum(lens)/len(lens):.1f}")


def cmd_merge(_args) -> None:
    if not AUG_FILE.exists():
        sys.exit(f"먼저 build 실행: {AUG_FILE}")
    target = TASK_DIR / "train.jsonl"
    before = sum(1 for _ in target.open())
    added = 0
    with target.open("a") as out, AUG_FILE.open() as f:
        for line in f:
            out.write(line)
            added += 1
    print(f"병합 완료: {before} → {before + added} (+{added})")


def main() -> None:
    ap = argparse.ArgumentParser()
    sub = ap.add_subparsers(dest="cmd", required=True)
    sub.add_parser("build")
    sub.add_parser("stats")
    sub.add_parser("merge")
    args = ap.parse_args()
    {"build": cmd_build, "stats": cmd_stats, "merge": cmd_merge}[args.cmd](args)


if __name__ == "__main__":
    main()
