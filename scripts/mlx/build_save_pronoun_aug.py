def make_record(input_text: str, ctx: str | None, date: str | None, answer: dict) -> dict:
    parts = [f"입력: {input_text}"]
    if ctx:
        parts.append(ctx)
    else:
        parts.append("맥락: 이전 발화 없음")
    if date:
        parts.append(f"날짜: {date}")
    user = "\n".join(parts)
    return {
        "messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": user},
            {"role": "assistant", "content": json.dumps(answer, ensure_ascii=False)},
        ]
    }


def cmd_build(_args) -> None:
    AUG_DIR.mkdir(parents=True, exist_ok=True)
    records = [make_record(*s) for s in SAMPLES]
    # 유효성 검증
    for i, r in enumerate(records):
        for m in r["messages"]:
            assert m["role"] in ("system", "user", "assistant")
            assert isinstance(m["content"], str) and m["content"]
        ast = next(m["content"] for m in r["messages"] if m["role"] == "assistant")
        parsed = json.loads(ast)
        assert set(parsed.keys()) <= {"text", "question"}
        assert len(parsed) == 1
    with AUG_FILE.open("w") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"작성 완료: {len(records)}건 → {AUG_FILE}")


def cmd_stats(_args) -> None:
    if not AUG_FILE.exists():
        sys.exit(f"먼저 build 실행: {AUG_FILE}")
    from collections import Counter
    keys = Counter()
    with AUG_FILE.open() as f:
        for line in f:
            d = json.loads(line)
            ans = json.loads(next(m["content"] for m in d["messages"] if m["role"] == "assistant"))
            keys["|".join(sorted(ans.keys()))] += 1
    print(f"총 {sum(keys.values())}건")
    for k, v in keys.most_common():
        print(f"  {k}: {v}")


def cmd_merge(_args) -> None:
    if not AUG_FILE.exists():
        sys.exit(f"먼저 build 실행: {AUG_FILE}")
    target = TASK_DIR / "train.jsonl"
    before = sum(1 for _ in target.open())
    added = 0
    with target.open("a") as out, AUG_FILE.open() as f:
        for line in f:
            out.write(line)
            added += 1
    print(f"병합 완료: {before} → {before + added} (+{added})")


def main() -> None:
    ap = argparse.ArgumentParser()
    sub = ap.add_subparsers(dest="cmd", required=True)
    sub.add_parser("build")
    sub.add_parser("stats")
    sub.add_parser("merge")
    args = ap.parse_args()
    {"build": cmd_build, "stats": cmd_stats, "merge": cmd_merge}[args.cmd](args)


if __name__ == "__main__":
    main()
